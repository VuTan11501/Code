function ICON(name, size = 16, extraClass = '') {
  const path = ICON_PATHS[name];
  if (!path) return '';
  const cls = extraClass ? ` ${extraClass}` : '';
  return `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round" class="icon${cls}" aria-hidden="true">${path}</svg>`;
}
// Shortcut: ICON.name(size)
for (const key of Object.keys(ICON_PATHS)) {
  ICON[key] = (size = 16, cls = '') => ICON(key, size, cls);
}

// Auto-inject icons into [data-icon] placeholders
function renderIcons(root = document) {
  const els = root.querySelectorAll('[data-icon]:not([data-icon-rendered])');
  els.forEach(el => {
    const name = el.getAttribute('data-icon');
    const size = parseInt(el.getAttribute('data-size') || '16', 10);
    const cls = el.getAttribute('data-icon-class') || '';
    if (ICON_PATHS[name]) {
      el.innerHTML = ICON(name, size, cls);
      el.setAttribute('data-icon-rendered', '1');
    }
  });
}
